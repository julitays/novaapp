export function getClientReview(emp) {
  const n = emp?.name || "";
  if (/Иван/.test(n)) {
    return "Клиент отмечает стабильную коммуникацию и более быструю реакцию на запросы. Предложены 2 инициативы по выкладке, эффект — +3% к доле полки.";
  }
  if (/Анна/.test(n)) {
    return "Аккуратная операционка, клиенты просят больше инициативности в промо-планировании и A/B гипотезах.";
  }
  if (/Дмитрий/.test(n)) {
    return "Чётко ведёт переговоры, умеет фиксировать договорённости. Рекомендуют усилить категорийную аналитику.";
  }
  return "Клиент удовлетворён качеством коммуникации; есть запрос на глубжее обоснование промо и запасов.";
}

export function getManagerFeedback(emp) {
  const n = emp?.name || "";
  if (/Иван/.test(n)) {
    return "Показывает стабильный прогресс. Усилить стратегический горизонт (2–3 квартала) и тренировать аналитические кейсы.";
  }
  if (/Анна/.test(n)) {
    return "Сильная дисциплина. Сфокусироваться на переговорах и работе с возражениями; дать наставника на 1 спринт.";
  }
  if (/Дмитрий/.test(n)) {
    return "Готов к расширенной ответственности. Рассмотреть GKAM при сохранении текущей динамики.";
  }
  return "Рекомендую углубить метрики unit-экономики и прокачать навык фасилитации совещаний с сетями.";
}
